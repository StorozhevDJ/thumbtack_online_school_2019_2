package net.thumbtack.school.concert.model;

public class Rating {

	private int rating;
	private String author;

	public int getComment() {
		return rating;
	}

	public void setComment(int comment) {
		this.rating = comment;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}
}
